namespace App\Form\Type;

use Symfony\Component\Form\AbstractType;
use Symfony\Component\Form\Extension\Core\Type\CheckboxType;
use Symfony\Component\Form\Extension\Core\Type\TextType;
use Symfony\Component\Form\FormBuilderInterface;
use Symfony\Component\OptionsResolver\OptionsResolver;
use Symfony\Component\Validator\Constraints\Length;
use Symfony\Component\Validator\Constraints\NotBlank;
use Symfony\Component\Validator\Constraints\Regex;

class VerificationCodeFormType extends AbstractType
{
    public function buildForm(FormBuilderInterface $builder, array $options)
    {
        $builder
            ->add('code', TextType::class, [
                'label' => 'Verification Code',
                'required' => true,
                'constraints' => [
                    new NotBlank([
                        'message' => 'Please enter the verification code',
                    ]),
                    new Length([
                        'min' => 6,
                        'max' => 6,
                        'exactMessage' => 'The verification code must be exactly {{ limit }} digits',
                    ]),
                    new Regex([
                        'pattern' => '/^\d{6}$/',
                        'message' => 'The verification code must contain only digits',
                    ]),
                ],
                'attr' => [
                    'placeholder' => '123456',
                    'autocomplete' => 'off',
                    'autofocus' => true,
                ],
            ])
            ->add('rememberDevice', CheckboxType::class, [
                'label' => 'Don’ask me again on this device',
                'required' => false,
            ]);
    }

    public function configureOptions(OptionsResolver $resolver)
    {
        $resolver->setDefaults([
            'data_class' => null,
        ]);
    }
} 